package tema3;

public class Materia
{
	private int clv, plan, ht, hp;
	private String cred;

	public int getHt()
	{
		return ht;
	}

	public void setHt(int ht)
	{
		this.ht = ht;
	}

	public int getHp()
	{
		return hp;
	}

	public void setHp(int hp)
	{
		this.hp = hp;
	}

	private String nom;

	public int getClv()
	{
		return clv;
	}

	public void setClv(int clv)
	{
		this.clv = clv;
	}

	public String getCred()
	{
		return cred;
	}

	public void setCred(String cred)
	{
		this.cred = cred;
	}

	public int getPlan()
	{
		return plan;
	}

	public void setPlan(int plan)
	{
		this.plan = plan;
	}

	public String getNom()
	{
		return nom;
	}

	public void setNom(String nom)
	{
		this.nom = nom;
	}

}
